    private void loadEmployeeDetails(String nic) {
        try {
            // Query to fetch employee details using NIC and inner joins
            String query = "SELECT e.nic, "
                    + "CONCAT(e.first_name, ' ', e.last_name) AS name, "
                    + "r.name AS role, " // Assuming roles table has 'role_name'
                    + "e.contact_info, "
                    + "g.name AS gender " // Assuming gender table has 'gender_name'
                    + "FROM employee e "
                    + "INNER JOIN roles r ON e.roles_id = r.id "
                    + "INNER JOIN gender g ON e.gender_id = g.id "
                    + "WHERE e.nic = '" + nic + "'";  // Filter by NIC

            // Execute the query using MySQL2
            ResultSet rs = MySQL2.executeSearch(query);

            // If data is available, populate the labels
            if (rs.next()) {
                nicLabel.setText(rs.getString("nic"));
                nameLabel.setText(rs.getString("name"));
                roleLabel.setText(rs.getString("role"));
                contactLabel.setText(rs.getString("contact_info"));
                genderLabel.setText(rs.getString("gender"));
            } else {
                // If no data is found, show a default message
                nameLabel.setText("No data found");
            }

            // Close the ResultSet
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

